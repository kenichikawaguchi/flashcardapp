## 3. ディープラーニング（深層学習）

ディープラーニングは機械学習の一分野で、**多層のニューラルネットワーク** を使って大量のデータから自動的に特徴を学習する技術です。

人間の脳の神経回路（ニューロン）を模倣した数学的モデルを多層に積み重ねることで、画像・音声・言語などの複雑なパターンを高精度で認識できます。

**主な活用分野：**
- 画像認識（顔認証・自動運転の物体認識）
- 音声認識（音声アシスタント・音声入力）
- 自然言語処理（翻訳・チャットボット・ChatGPT）
- 異常検知（製造ライン・金融取引の不正検知）

