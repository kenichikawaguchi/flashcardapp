## 6. 主なデジタル技術キーワード

| 技術 | 内容 |
|------|------|
| IoT（モノのインターネット） | 様々なモノをネット接続してデータを収集・制御 |
| ブロックチェーン | 改ざん困難な分散型台帳技術（仮想通貨・スマートコントラクト） |
| AR（拡張現実） | 現実の映像にデジタル情報を重ねる技術 |
| VR（仮想現実） | 完全な仮想空間を体験する技術 |
| RPA（ロボティクスプロセスオートメーション） | 定型的なPC作業をソフトウェアロボットが自動実行 |
| 5G | 高速大容量・低遅延・多数同時接続の第5世代移動通信 |

